            cursor = conn.cursor()

            sql_command = "UPDATE cont SET " + self.cont_update_comboBox.currentText() + " = \'" + self.cont_nouaValoare_lineEdit.text() + "\' WHERE Pacient_ID_pacient = " + self.cont_updateID_lineEdit.text()

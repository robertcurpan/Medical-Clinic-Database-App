    def Insert(self):
        if self.intmed_nrInternare_lineEdit.text() != "" and self.intmed_IDmedicament_lineEdit.text() != "" and self.intmed_cantitate_lineEdit.text() != "" and self.intmed_data_lineEdit.text() != "":
            sql_command = "INSERT INTO internare_medicament_relmxn VALUES (\'" + self.intmed_nrInternare_lineEdit.text() + "\', \'" + self.intmed_IDmedicament_lineEdit.text() + "\', \'" + self.intmed_cantitate_lineEdit.text() + "\', \'" + self.intmed_data_lineEdit.text() + "\')"
            cursor = conn.cursor()

            try: ################################ TRANZACTIE ##################################
                cursor.execute(sql_command)

                cursor.execute("SELECT pret, denumire FROM medicament WHERE ID_medicament = \'" + self.intmed_IDmedicament_lineEdit.text() + "\'")
                for val in cursor:
                    pret_medicament = int(val[0])
                    denumire_medicament = val[1]

                cantitate = int(self.intmed_cantitate_lineEdit.text())

                cursor.execute("SELECT Pacient_ID_pacient FROM internare WHERE nr_internare = " + self.intmed_nrInternare_lineEdit.text())
                for val in cursor:
                    pacient_id = val[0]

                cursor.execute("SELECT suma_disponibila FROM cont WHERE Pacient_ID_pacient = " + str(pacient_id))
                for val in cursor:
                    suma_disp = int(val[0])

                cursor.execute("SELECT Manopera_ID_manopera FROM internare WHERE nr_internare = " + self.intmed_nrInternare_lineEdit.text())
                for val in cursor:
                    manopera_id = val[0]

                cursor.execute("SELECT pret, tip FROM manopera WHERE ID_manopera = \'" + str(manopera_id) + "\'")
                for val in cursor:
                    pret_manopera = int(val[0])
                    denumire_manopera = val[1]

                if suma_disp >= pret_medicament * cantitate + pret_manopera:
                    cursor.execute("UPDATE cont SET suma_disponibila = " + str(suma_disp - pret_medicament * cantitate - pret_manopera) + " WHERE Pacient_ID_pacient = \'" + str(pacient_id) + "\'")
                    cursor.execute("COMMIT")
                    self.intmed_feedback_textBrowser.append("Tranzactie realizata. Suma disponibila din contul pacientului cu ID-ul " +
                                                            str(pacient_id) + " era: " + str(suma_disp) + ", iar acum este: " +
                                                            str(suma_disp - pret_medicament * cantitate - pret_manopera) +
                                                            " deoarece pacientul a beneficiat de medicamentele: " + str(cantitate) + "x " + str(denumire_medicament) +
                                                            " (" + str(pret_medicament) + " lei) si manopera: " + str(denumire_manopera) + " (" + str(pret_manopera) + " lei)")

            except Exception as e:
                self.intmed_feedback_textBrowser.append(str(e))
        else:
            self.intmed_feedback_textBrowser.append("Camp obligatoriu necompletat!")


